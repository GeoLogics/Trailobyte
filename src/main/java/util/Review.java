package util;

public class Review {

	public String author;
	public String trailName;
	public String comment;
	public double rating;
	
	
	public Review() {
		
	}
	
	public Review(String author, String trailName, String comment, double rating) {
		this.author = author;
		this.trailName = trailName;
		this.comment = comment;
		this.rating = rating;
	}
	
	
	
	
}
